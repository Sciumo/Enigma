package enigma.event;

/**
 * An abstract implementation of the <code>TextMouseListener</code> class,
 * with all methods stubbed out.
 *
 *@status.stable
 *@see TextMouseEvent 
 *@see TextMouseListener
 */
public abstract class TextMouseAdapter implements TextMouseListener {
    public void mousePressed(TextMouseEvent e) {}

    public void mouseReleased(TextMouseEvent e) {}

    public void mouseClicked(TextMouseEvent e) { }
}
